package Modelo.Ejercicios;

public class Equipamento {
    private int id;
    private String name;

    public Equipamento() {
    }

    public Equipamento(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
}
