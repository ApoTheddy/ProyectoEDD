    public void rellenarComidas() {
        ArrayList<Receta> recetas = new ArrayList<>();
        for (int i = 0; i < 20; i++) {
            Receta receta = api.getRandomRecipe();
            recetas.add(receta);
        }
        
        for (int i = 0; i < recetas.size(); ++i) {
            pnlComidas.add(new CardComida(recetas.get(i)));
        }
    }

